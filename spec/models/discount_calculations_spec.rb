      @cart = Cart.new({
        @ogre.id.to_s => 1,
        @giant.id.to_s => 2
        })
    end

    it '.check_if_discount_unique' do

      expect(DiscountCalculations.new.check_if_discount_unique(@ogre.id, 5, 10)).to eq(true)
      @discount_1 = @giant.discounts.create!(minimum_amount: 2, discount_amount: 10)
